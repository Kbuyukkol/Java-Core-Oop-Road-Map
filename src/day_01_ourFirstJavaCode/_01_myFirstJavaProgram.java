public class _01_myFirstJavaProgram {

	/**
	 * @param args
	 */
	
	//Every Java application must contain a main method whose signature looks like this:
	//
	//			public static void main(String[] args){}
	//
	//When the java interpreter executes and application (by being invoke upon the application's
	//controlling class), it starts by calling the class's main method. The main method then calls
	//all the other methods required to run your application.
	
	public static void main(String[] args)/* Main Method */ { // Opening Braces 
		
		System.out.println("Maraba Zalım Dünya!") /* Displays a message on the screen */  ; //Semicolon 
	} // Closing Braces

}
