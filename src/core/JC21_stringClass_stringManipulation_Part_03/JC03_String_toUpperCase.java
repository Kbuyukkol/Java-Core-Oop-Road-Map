public class JC03_String_toUpperCase {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		/**
		 * toUpperCase() Method
		 * 
		 * --> toUpperCase() method returns the calling string value converted to
		 * uppercase.
		 */

		String word = "automation";
		System.out.println(word.toUpperCase());

		String str = "New world design";
		System.out.println(str.toUpperCase());
	}

}
