public class _02_basicLoopExamples {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		for (int i = 0; i < 10; i += 1) {
			System.out.println("Hello ");
		}

		System.out.println("\n+---------------------------------\n");

		for (int i = 1; i <= 10; i++) {
			System.out.println("2 X " + i + " = " + (2 * i));
		}
	}

}
