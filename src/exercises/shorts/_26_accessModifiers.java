    /**
     * ACCESS MODIFIERS
     *
     * There are 4 access modifiers available in JAVA
     *
     * - public         : Accessible from; Class, Package, SubClass, World (4)
     * - protected      : Accessible from; Class, Package, SubClass (3)
     * - default        : Accessible from; Class, Package (2)
     * - private        : Accessible from; Class. (1)
     *
     * - Top level of Java class can have just 2 access modifiers, 1. public, 2. default
     * - Variables, constructors and methods can have all four access modifiers.
     */

