package exercises.shorts;

public class _24_statics {

    /**
     * STATICS
     * - Static member in java are can be variables, methods, class static blocks
     * - Declared with static keyword
     * - Belongs to the class, and can bee called through the class
     * - There is only one copy of the static for all objects of the class
     * - All the objects of the class shares the same copy.
     *
     *
     * STATIC VARIABLE
     * - Refer to the common property of all objects
     * - It gets memory only one in the class area at the time of class loading.
     * - A single copy of a class's static variable is shared by all objects of the class.
     * - If changes are made to static variables, all other instances will see the effect of the change.
     *
     * STATIC METHODS
     * - If we declare any methods as static. it's known as static method.
     * - A method that can be called using class name, without creating objects.
     * - Objects can also call static methods.
     * - Static methods only accepts static members.
     *
     */
}
